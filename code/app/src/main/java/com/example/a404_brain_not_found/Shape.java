package com.example.a404_brain_not_found;

public abstract class Shape {

    protected int x;
    protected int y;
    protected String color = "blue";

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
